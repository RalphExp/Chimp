func printParserErrors(out io.Writer, errors []string) {
	io.WriteString(out, "Woops! We ran into some chimp business here!\n")
	io.WriteString(out, " parser errors:\n")
	for _, msg := range errors {
		io.WriteString(out, "\t"+msg+"\n")
	}
}
